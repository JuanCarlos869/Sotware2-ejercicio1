    private void jBtnCalcularCirculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCalcularCirculoActionPerformed
        // Verificar si el textField está vacío
        if (jTxtRadioCirculo.getText().isEmpty()) {
            // Manejo de errores en caso de que la cadena no pueda ser parseada a double
            JOptionPane.showMessageDialog(null, "Campos vacios, llene los campos para continuar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double radius= 0.0; // Valor por defecto o manejo de error adecuado según tu lógica
        try {
            radius = Double.parseDouble(jTxtRadioCirculo.getText());
        } catch (NumberFormatException e) {
            // Manejo de errores en caso de que la cadena no pueda ser parseada a double
            JOptionPane.showMessageDialog(null, "Error, datos invalidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        figure = new Circle(radius);
        jLblAreaCirculo.setText("Area: " + figure.calculateArea());
        jLblPerimetroCirculo.setText("Perimetro: " + figure.calculatePerimeter());
    }//GEN-LAST:event_jBtnCalcularCirculoActionPerformed

