    private void jBtnCalcularTrianguloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCalcularTrianguloActionPerformed
        // Verificar si el textField está vacío
        if (jTxtBaseTriangulo.getText().isEmpty() || jTxtAlturaTriangulo.getText().isEmpty()) {
            // Manejo de errores en caso de que la cadena no pueda ser parseada a double
            JOptionPane.showMessageDialog(null, "Campos vacios, llene los campos para continuar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double base= 0.0; // Valor por defecto o manejo de error adecuado según tu lógica
        double altura= 0.0; // Valor por defecto o manejo de error adecuado según tu lógica
        try {
            base = Double.parseDouble(jTxtBaseTriangulo.getText());
            altura = Double.parseDouble(jTxtAlturaTriangulo.getText());
        } catch (NumberFormatException e) {
            // Manejo de errores en caso de que la cadena no pueda ser parseada a double
            JOptionPane.showMessageDialog(null, "Error, datos invalidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        figure = new Triangle(base,altura);
        jLblAreaTriangulo.setText("Area: " + figure.calculateArea());
        jLblPerimetroTriangulo.setText("Perimetro: " + figure.calculatePerimeter());
    }//GEN-LAST:event_jBtnCalcularTrianguloActionPerformed

